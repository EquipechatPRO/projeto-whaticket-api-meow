/**
 * Translation hook that reads current language from settings store
 */
export function useTranslation() {
  const language = useSettingsStore((s) => s.language);
  const dict = translations[language] || translations["pt-BR"];

  /**
   * Get translated string by key, with optional interpolation
   * t("dashboard.of_conversations", "10") → "de 10 conversas"
   */
  const t = (key: string, ...args: (string | number)[]): string => {
    let str = dict[key] || translations["pt-BR"][key] || key;
    args.forEach((arg, i) => {
      str = str.replace(`{${i}}`, String(arg));
    });
    return str;
  };

  return { t, language };
}
